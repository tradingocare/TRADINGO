CREATE DATABASE tradingo;
CREATE USER tradingo_app WITH PASSWORD '${DB_PASSWORD}';
GRANT CONNECT ON DATABASE tradingo TO tradingo_app;
GRANT USAGE ON SCHEMA public TO tradingo_app;
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA public TO tradingo_app;
ALTER DEFAULT PRIVILEGES IN SCHEMA public GRANT SELECT, INSERT, UPDATE, DELETE ON TABLES TO tradingo_app;
CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
ALTER DATABASE tradingo SET statement_timeout = 30000;
ALTER DATABASE tradingo SET idle_in_session_timeout = 60000;
ALTER DATABASE tradingo SET lock_timeout = 15000;
